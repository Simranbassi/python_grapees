number=int(input("enter any number : "))
if number==1:
	print("MONDAY")
elif number==2:
	print("TUESDAY")
elif number==3:
	print("WEDNESDAY")
elif number==4:
	print("THURSDAY")	
elif number==5:
	print("FRIDAY")
elif number==6:
	print("SATURDAY")
elif number==7:
	print("SUNDAY")
else:
	print("invalid day")